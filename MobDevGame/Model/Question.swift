// Import Foundation for fundamental data types and common operations.
import Foundation
// Import UIKit for access to UIColor and UIImage.
import UIKit

// Define a struct named `Question`.
struct Question {
    // Properties of the struct.
    
    // The text of the question.
    let text: String
    
    // The answer to the question.
    let answer: String
    
    // The color associated with the question.
    let color: UIColor
    
    // The image associated with the question.
    let image: UIImage
}

